#函数式编程的应用

#函数式编程是指将持续的行为抽象出来，作为基本的元素，以此组织程序。它来自于与图灵机同时提出的Lambda计算模型

#在函数式编程中，最著名的特殊就是高序(High order)，简单的说就是定制一个算法，按规则来指定容器中的每一个元素，最常用的high order
#为：
#1.映射：也就是将算法试与每一个元素，将返回值合并为一个新的容器，               若map函数
#2.过滤，将算法施于每个元素，将返回值为真的元素的合并为一个新的容器       filter函数
#3.合并，将算法（可能携带一个初值）依次施于每个元素，将返回值作为下一步计算的参数之一，与下一个元素再计算，直至最终结果  reduce函数

